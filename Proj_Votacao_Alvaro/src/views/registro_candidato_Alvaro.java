    private void botaoRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRegistrarActionPerformed
        try {
            novo.validaEntradaCandidato(textNome.getText(), textNumero.getText());
            String nome= textNome.getText();
            int numero= Integer.parseInt(textNumero.getText());
            Candidato teste = new Candidato(nome, numero);
            novo.incluir(teste);
            textNome.setText("");
            textNumero.setText("");     
            JOptionPane.showMessageDialog(rootPane, "Dados registrados!");
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_botaoRegistrarActionPerformed

